// Declare a constant variable named 'myString' and assign it a string value.
const myString = "This is a string"

// Use the .length property to find out the length of myString.
console.log(`The length of myString is ${myString.length}`);

// Use bracket notation to find out the first character of myString.
console.log(`The first character of myString is ${myString[0]}`);

// Use bracket notation to find out the 11th character of my String.
console.log(`The 11th character of myString is ${myString[10]}`);

